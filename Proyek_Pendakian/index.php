                <h2>Sejarah</h2>
                <img src="assets/img/puncak lawu hargo dumilah 3265 mdpl (1) (1).jpeg" alt="Sejarah Gunung Lawu" class="featured-image">
                <p>
                    Gunung Lawu merupakan salah satu gunung yang sarat dengan nilai sejarah dan mistis. Terletak di perbatasan Jawa Tengah
                    dan Jawa Timur, Gunung Lawu dianggap sebagai tempat sakral yang dipercaya menjadi lokasi semedi Prabu Brawijaya V,
                    raja terakhir Majapahit. Gunung ini juga menjadi saksi sejarah perkembangan budaya Jawa, dengan banyak ditemukan
                    situs-situs kuno seperti Candi Cetho dan Candi Sukuh yang berfungsi sebagai tempat peribadatan pada masa lampau.
                </p>
