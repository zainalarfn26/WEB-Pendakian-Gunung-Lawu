            <article id="Geografis" class="card">
                <h2>Geografis</h2>
                <img src="assets/img/Sabana Gupak Menjangan.jpeg" alt="Pemandangan Geografis Gunung Lawu" class="featured-image">
                <p>Gunung Lawu memiliki ketinggian 3.265 meter di atas permukaan laut dan dikelilingi oleh tiga kabupaten, yaitu
                    Karanganyar (Jawa Tengah), Magetan, dan Ngawi (Jawa Timur). Gunung ini dikenal memiliki tiga puncak utama, yaitu
                    Hargo Dalem, Hargo Dumilah, dan Hargo Dumiling. Kawasan Gunung Lawu memiliki vegetasi yang beragam, dari hutan pinus
                    di kaki gunung hingga padang rumput dan edelweis di puncaknya. Selain itu, Lawu juga memiliki beberapa jalur
                    pendakian terkenal seperti Cemoro Sewu, Cemoro Kandang, dan Candi Cetho.
                </p>
            </article>

